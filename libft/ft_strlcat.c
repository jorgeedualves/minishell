#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	len_dst;
	size_t	len_src;

	i = 0;
	len_dst = ft_strlen(dst);
	len_src = ft_strlen(src);
	if (size <= len_dst)
		return (size + len_src);
	while (dst[i])
		i++;
	while (*src && i < size - 1)
		dst[i++] = *(src++);
	dst[i] = '\0';
	return (len_dst + len_src);
}
